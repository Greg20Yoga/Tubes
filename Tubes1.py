# Define defini subprogram
def wrap_text(text, width):
    lines = []
    for i in range(0, len(text), width):
        lines.append(text[i:i + width])
    return lines

def create_receipt(items, total):
    receipt = []
    max_item_length = max(len(item) for item, _ in items)
    max_price_length = max(len(f"Rp {price:.2f}") for _, price in items)
    line_width = max(max_item_length + max_price_length + 10, len("THANK YOU FOR USING OUR SERVICES. RATE US 5 STARS"))

    receipt.append("‚ïî" + "‚ïê" * line_width + "‚ïó")
    receipt.append(f"‚ïë{'RECEIPT':^{line_width}}‚ïë")
    receipt.append("‚ï†" + "‚ïê" * line_width + "‚ï£")

    for item, price in items:
        wrapped_item = wrap_text(item, max_item_length)
        wrapped_price = wrap_text(f"Rp {price:.2f}", max_price_length)
        for i in range(max(len(wrapped_item), len(wrapped_price))):
            item_text = wrapped_item[i] if i < len(wrapped_item) else ""
            price_text = wrapped_price[i] if i < len(wrapped_price) else ""
            receipt_line = f"‚ïë {item_text:<{max_item_length}} ‚îÇ {price_text:<{max_price_length}}{' ' * (line_width - max_item_length - max_price_length - 4)}‚ïë"
            receipt.append(receipt_line)

    receipt.extend([
        "‚ï†" + "‚ïê" * line_width + "‚ï£",
        f"‚ïë{'Total:':<{max_item_length+1}} ‚îÇ Rp {total:.2f}{' ' * (line_width - max_item_length - max_price_length - 4)}‚ïë",
        "‚ï†" + "‚ïê" * line_width + "‚ï£",
        "‚ï†" + "‚ïê" * line_width + "‚ï£",
        f"‚ïë‚çü ‚çü ‚çü  THANK YOU FOR USING OUR SERVICES ‚çü ‚çü ‚çü{' ' * (line_width - len('‚çü ‚çü ‚çü THANK YOU FOR USING OUR SERVICES ‚çü ‚çü ‚çü')-1)}‚ïë",
        "‚ïö" + "‚ïê" * line_width + "‚ïù",
    ])

    return receipt
